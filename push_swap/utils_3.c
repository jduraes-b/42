int	cheapest(t_stack *s)
{
	int	i;
	int	temp;

	temp = INT_MAX;
	i = 0;
	while (s)
	{
		if (s->cost < temp)
		{
			temp = s->cost;
			i = s->position;
		}
		s = s->next;
	}
	return (i);
}
